    public interface OnFragmentInteractionListener {
        // TODO: Update argument type and name
        void onFragmentInteraction(Uri uri);
    }

    private void send() {
        if (null == username) return;
        if (!socket.connected()) return;

        String message = inputMessageView.getText().toString().trim();
        if (TextUtils.isEmpty(message)) {
            inputMessageView.requestFocus();
            return;
        }

        inputMessageView.setText("");
        //addMessage(username, message);

        // perform the sending message attempt.
        socket.emit("new message", message);
    }

    private Emitter.Listener onConnect = new Emitter.Listener() {
        @Override
        public void call(Object... args) {
            getActivity().runOnUiThread(new Runnable() {
                @Override
                public void run() {
                    if(!isConnected) {
                        if(null != username) {
                            socket.emit("add user", username);
                            Toast.makeText(getActivity().getApplicationContext(), "new user", Toast.LENGTH_LONG).show();
                            isConnected = true;


                        }
                    }
                }
            });
        }
    };

    private Emitter.Listener onNewMessage = new Emitter.Listener() {
        @Override
        public void call(final Object... args) {
            getActivity().runOnUiThread(new Runnable() {
                @Override
                public void run() {
                    JSONObject data = (JSONObject) args[0];
                    String username;
                    String message;
                    try {
                        username = data.getString("username");
                        message = data.getString("message");
                    } catch (JSONException e) {
                        Log.e("messageyo", e.getMessage());
                        return;
                    }

                    //removeTyping(username);
                    //addMessage(username, message);
                }
            });
        }
    };
